						<div class="title-line">Rules for Operation</div>
						<ul class="nopad">
							<li>
								<strong>Chapter 1 General provisions</strong>
								<ul>
									<li>
										<strong>Article 1 (Purpose)</strong>
										<ul><li>The purpose of this rules for operation is to stipulate the extent to which all benefits such as money and valuables, entertainment and convenience allowed from stakeholders and the standard of conduct to be observed for the efficient operation of Code of Ethics.</li></ul>
									</li>
									<li>
										<strong>Article 2 (Application scope)</strong>
										<ul><li>This standard applies to all employees of the company.</li></ul>
									</li>
									<li>
										<strong>Article 3 (Definition of a term)</strong>
										<ul><li>Definition of a term used in this rules for operation are as follows.</li></ul>
										<ul>
											<li><span>1.</span> Money and valuables: Money (cash, securities, gift certificates, coupons etc), economic benefits such as goods</li>
											<li><span>2.</span> Entertainment: Provision of treat such as meals, drinking, golf, performance, entertainment, use of horizontal entertainment</li>
											<li><span>3.</span> Convenience: transportation, accommodation, tourism, support for event, employment, job placement etc</li>
											<li><span>4.</span> Stakeholders: Employees, customers, business partners, affiliates, domestic and foreign public officials and other organizations who may be directly or indirectly affected by their rights, interest or obligation due to their work.</li>
										</ul>
									</li>
								</ul>

							</li>

							<li>
								<strong>Chapter 2 Compliance</strong>
								<ul>
									<li>
										<strong>Article 4 (Prohibition of accepting bribery)</strong>
										<ul class="num">
											<li><span>1.</span> Employees shall not receive or request money or other valuables from stakeholders for any reason. However, souvenirs and promotional items distributed to an unspecified number of people within the normal level (30,000 won) are excluded.</li>
											<li><span>2.</span> If employees inevitably receive money or other valuables, they must return it immediately.</li>
											<li><span>3.</span> If it is unavoidably difficult to return the money or valuables received, employees shall keep the money and valuables in a way that they do not mix with their own property and report them to the head of their department.</li>
											<li><span>4.</span> Upon receiving the above report, head of the department must report to the department in charge of ethical management immediately and follow the instructions of the department in charge of ethical management.</li>
										</ul>
									</li>
									<li>
										<strong>Article 5 (Prohibition of entertainment)</strong>
										<ul class="num">
											<li><span>1.</span> Employees can treat or be treated with meals within the usual level (30,000won) with stakeholders. However, industries that combine personal services such as entertainment bars and massage is excluded.</li>
											<li><span>2.</span> If employees inevitable get treated that exceeds the normal level, they must report it to the head of department.</li>
											<li><span>3.</span> If employees provide entertainment in excess of the usual level to stakeholders or participate in stakeholders’ event that include entertainment in excess of the usual level, they must report to the head of department in advance.</li>
										</ul>
									</li>
									<li>
										<strong>Article 6 (Prohibition of convenience provision)</strong>
										<ul class="num">

											<li><span>1.</span> Employees should not receive conveniences such as transportation, accommodation and tourism from stakeholders. Conveniences generally provided to all participants at events organized by stakeholders is excluded.</li>
											<li><span>2.</span> If an employee is provided with a convenience that inevitably exceeds the permitted range, they shall report it to the head of department.</li>
											<li><span>3.</span> In all cases, direct or indirect, employees must report to the head of department about any special accommodations provided or offered to them.</li>


										</ul>
									</li>
									<li>
										<strong>Article 7 (Expenditure for congratulations and condolences)</strong>
										<ul class="num">
											<li><span>1.</span> In principle, employees’ congratulations and condolences should not be notified to the stakeholders and it is considered that the information notified through a third party is employees own responsibility.</li>
											<li><span>2.</span> It is a principle that employees do not receive congratulations and condolences from stakeholders. If employees inevitably receive congratulations and condolences, it is recommended to be within 50,000 won. If it exceeds 100,000won, employees must return some or all of it.</li>
											<li><span>3.</span> Employees should not receive wreath for congratulations and condolences from stakeholders and even if received unavoidably, they are not allowed to exhibit it.</li>
											<li><span>4.</span> Employees can use company’s bulletin board to announce congratulations and condolences. However, the scope is limited to the grandparents, parents, children and spouse.</li>
											<li><span>5.</span> Congratulations and condolences between employees are recommended to be within 50,000 won and should not exceed 100,000 won in any special cases.</li>

										</ul>
									</li>
									<li>
										<strong>Article 8 (Prohibition of sponsorship)</strong>
										<ul class="num">
											<li><span>1.</span> In case of events supported by the company such as departmental events or club activities, money or valuables should not be received from stakeholders.</li>
											<li><span>2.</span> It is considered that money and valuables were received for receiving convenience such as vehicles, places and personnel related to event.</li>
											<li><span>3.</span> If an employee inevitably receive support for an event, they must report to the head of their department immediately.</li>
										</ul>
									</li>
									<li>
										<strong>Article 9 (Restrictions on transactions using job-related information)</strong>
										<ul><li><span>Em</span>ployees shall not engage in any transaction or investment in property related to securities or real estate using undisclosed information acquired from work or to assist in trading or investment in property by providing such information to others.</li></ul>

									</li>
									<li>
										<strong>Article 10 (Secrecy)</strong>
										<ul class="num">
											<li><span>1.</span> Employees who have acquired the inside information of the company must abide by the duty of confidentiality, recognizing that the leakage or early leakage of the information may infringe the company’s interest.</li>
											<li><span>2.</span> Such information include people, products, research, technical projects, industrial data, sales and financial planning and any factors that could cause leakage of industrial property rights and know-how.</li>
											<li><span>3.</span> Employees should be careful not to leak such information to outside or to internal employees who are not subject to information sharing.</li>
										</ul>
									</li>





									<li>
										<strong>Article 11 (Budget use)</strong>
										<ul class="num">
											<li><span>1.</span> Budgets for each department such as meeting expenses and work promotion expenses should be used in accordance with the purpose of budget establishment.</li>
											<li><span>2.</span> In principle, corporate credit card is used for execute expense, it is prohibited to use a corporate credit card for public holidays and personal use.</li>
										</ul>
									</li>
									<li>
										<strong>Article 12 (Financial dealings)</strong>
										<ul class="num">
											<li><span>1.</span> Employees should not engage in legal acts with stakeholders such as lending money, guaranteeing loans or leasing real estate.</li>
											<li><span>2.</span> If employees inevitably commit such legal acts with stakeholders, they must report to the head of department.</li>
										</ul>
									</li>
									<li>
										<strong>Article 13 (Exclusion of special treatment and discrimination)</strong>
										<ul><li>Employees shall not give special treatment to a specific person or discriminate against a specific person because of regionalism, blood relationship, school tie or religion in performing their duties.</li></ul>
									</li>
								</ul>
							</li>
							<li>
								<strong>Chapter 3 Responsibility and authority</strong>
								<ul>
									<li>
										<strong>Article 14 (Responsibility of department supervisor)</strong>
										<ul class="num">
											<li><span>1.</span> The head of department should conduct training and counseling frequently so that employees can fully understand rules for operation.</li>
											<li><span>2.</span> Head of the department should take appropriate precautions to prevent its employees from violating rules for operation.</li>
										</ul>
									</li>
									<li>
										<strong>Article 15 (Reporting obligations and confidentiality)</strong>
										<ul class="num">
											<li><span>1.</span> Employees must report to the head of their department or to the department in charge of ethical management if they find out that they or other employees have violated rules for operation.</li>
											<li><span>2.</span> The head of department must report to the department in charge of ethical management immediately upon receiving a report of the violation.</li>
											<li><span>3.</span> No one shall divulge identity of the reporter or inflict disadvantage.</li>
											<li><span>4.</span> Anyone who discloses the identity of the reporter or inflicts disadvantage in violation of paragraph 3 above shall be subject to disciplinary action.</li>
										</ul>
									</li>
								</ul>
							</li>
							<li>
								<strong>Chapter 4 Reward and disciplinary action</strong>
								<ul>

									<li>
										<strong>Article 16 (Reward and disciplinary action)</strong>
										<ul class="num">
											<li><span>1.</span> In accordance with the provision, company may reward employees who made contributions to achieving the purpose of rules for operation</li>
											<li><span>2.</span> Company may severely reprimand and discipline employees who violate the rules for operation in accordance with regulation.</li>
											<li><span>3.</span> Company may restrict access and transactions to employees who retired in violation of these rules for operation.</li>
										</ul>
									</li>
									<li>
										<strong>Article 17 (Interpretation)</strong>
										<ul class="num">
											<li><span>1.</span> If an employee violates these guidelines by using relatives or acquaintances, such an act is regarded as an act of the employee.</li>
											<li><span>2.</span> For rules for operation that is not specifically determined, relevant laws and ordinances will be followed.</li>
										</ul>
									</li>

 

									<li>
										<strong>Article 18 (Operation of ethics management report center)</strong>

										<ul>
											<li><span>St</span>rengthening the foundation for ethical management through prevention of internal fraud and risk factors in timely recognition</li>

											<li><strong>1. Report center (Homepage report center)</strong>
												<ul class="dot">
													<li>Receiving reports from employees/customers/shareholders/partners regarding unethical conduct both internally and externally</li>
													<li>Consultation on ethical issues</li>
													<li>Internal Public E-mail for report (<a href="mailto:ethics@nexentire.com">ethics@nexentire.com</a>)</li>
													<li>External Public E-mail for report (<a href="mailto:nexenethicscenter@gmail.com">nexenethicscenter@gmail.com</a>)</li>

												</ul>
											</li>


											<li><strong>2. Informant protection program (Informant protection program)</strong>
