					<div class="content-box">

						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 1. Mixing Process</div>
								<div class="txt">The &lsquo;mixing Process&rsquo; involves adding and mixing
									various chemicals to crude rubber according to the characteristics and
									intended use of each tire. Rubber sheets are created by adding
									plasticity to rubber so that its shape does not change easily.</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_1.jpg"></div>
						</div>
						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 2. Extrusion Process</div>
								<div class="txt">Once rubber sheets are made according to the
									characteristics of each part of the tire through the &lsquo;mixing
									Process,&rsquo; rubber is then created into a certain regular width and
									thickness through the &lsquo;extrusion process.&rsquo; Here, the treads
									(outer surface of the tire) and sidewalls (sides of the tire) are
									created, which is an important process for cozy and comfortable driving.
								</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_2.jpg"></div>
						</div>

						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 3. Calendering Process</div>
								<div class="txt">The &lsquo;calendering Process&rsquo; involves evenly
									applying and thinly topping a certain thickness of rubber sheets created
									in the &lsquo;mixing Process&rsquo; on both sides of steel cords and
									fabric cords. Rubber is coated on the front and back by pushing steel
									cords and fabric cords through the constantly rolling rollers. Tires can
									endure the car weight and maintain a certain form due to this
									&lsquo;calendering Process&rsquo; (cords).</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_3.jpg"></div>
						</div>
						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 4. Bead Process</div>
								<div class="txt">The &lsquo;bead process&rsquo; is the process of coating
									steel wires with rubber multiple times in a certain thickness, and
									attaching filler rubber here. Beads fix the rubber of the tires to the
									wheels, serving as the frame of tire rims and thus making the tires
									stronger if there are more beads.</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_4.jpg"></div>
						</div>

						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 5. Building Process</div>
								<div class="txt">Now, there is the &lsquo;building Process&rsquo; in which
									cylindrical green tires are made by consecutively attaching all
									components and materials used in tires to the molding press. This
									&lsquo;building Process&rsquo; requires a great of attention since it
									determines the quality of the tires. Radial tires used in automobiles
									first require the process of attaching the &lsquo;carcass&rsquo; (the
									frame of the tire located inside the tread), &lsquo;beads&rsquo; (the
									frame fixed on the rim of wheels), and &lsquo;sidewalls.&rsquo; Then,
									they are transferred to the secondary molding press to complete tires by
									attaching belts and treads.</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_5.jpg"></div>
						</div>

						<div class="step-box">
							<div class="Lbox">
								<div class="title">Step 6. Curing Process</div>
								<div class="txt">The &lsquo;curing process&rsquo; involves adding heat and
									pressure inside and outside by putting tires made of flexible rubber in
									a fixed mold. Sulfur and other chemicals react to rubber and create a
									unique design of NEXEN TIRE on the tread, and also apply elasticity to
									the rubber. Also, they provide any desired structure, shape, exterior,
									and mechanical/chemical characteristics.</div>
							</div>
							<div class="Rbox"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_6.jpg"></div>
						</div>
						<div class="step-box">
							<div class="title">Step 7. Test Process and Shipping </div>
							<div class="image-box"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_7_1.jpg"><img alt=""
									src="__icsFiles/artimage/2017/01/24/cnt_311/img_b_sense1_7_2.jpg"></div>
							<div class="txt">The &lsquo;test process&rsquo; consists of five detailed
								tests.<br>First, there is the &lsquo;visual test&rsquo; that inspects the
								exterior and interior of the tire as well as the bead with the naked eye.
								Second, there is the testing of the tire as a single product before
								assembling on the wheel, and also whether the tire weight distribution is
								consistent in the circumferential direction (*direction of ultrasonic beam
								that is vertical to the main shaft of the cylindrical device) in the static
								state. Third, there is the &lsquo;dynamic balance test&rsquo; that measures
								the weight uniformity in the circumferential direction after rotation when
								the tires are assembled on the wheels and optimal air pressure is put into
								the tires. Fourth, there is the &lsquo;uniformity test&rsquo; that tests the
								uniformity of size and rigidity of the product. Finally, x-rays are used to
								test the carcass inside the tires, structure of belts, and mixing of foreign
								matters, after which the manufactured products are automatically classified
								and shipped.</div>
						</div>

